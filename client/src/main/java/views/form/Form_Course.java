    private void loadCourses() {
        try {
            List<Course> courses = courseService.getAllCourses(); // Lấy danh sách khóa học từ server
            DefaultTableModel model = (DefaultTableModel) table1.getModel();
            model.setRowCount(0); // Xóa dữ liệu cũ trước khi nạp mới

            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy"); // Định dạng ngày

            for (Course course : courses) {
                // Chuyển đổi ngày bắt đầu và ngày kết thúc thành String để hiển thị trên bảng
                String beginDateStr = (course.getBeginDate() != null) ? dateFormat.format(course.getBeginDate()) : "N/A";
                String endDateStr = (course.getEndDate() != null) ? dateFormat.format(course.getEndDate()) : "N/A";

                model.addRow(new Object[]{
                    course.getId(),
                    course.getCourseName(),
                    beginDateStr, // Hiển thị ngày bắt đầu dưới dạng chuỗi
                    endDateStr // Hiển thị ngày kết thúc dưới dạng chuỗi
                });
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(this, "Lỗi tải danh sách khóa học: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void filterTable(String keyword) {
        DefaultTableModel model = (DefaultTableModel) table1.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        table1.setRowSorter(sorter);

        if (keyword.isEmpty()) {
            sorter.setRowFilter(null); // Hiển thị toàn bộ dữ liệu nếu ô tìm kiếm rỗng
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + keyword, 1)); // Lọc theo cột thứ 2 (cột tên giảng viên)
        }
    }

