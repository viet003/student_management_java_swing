    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        int selectedRow = table1.getSelectedRow();
        if (selectedRow != -1) {
            Object idObj = table1.getValueAt(selectedRow, 0);
            Object nameObj = table1.getValueAt(selectedRow, 1);
            Object beginDateObj = table1.getValueAt(selectedRow, 2);
            Object endDateObj = table1.getValueAt(selectedRow, 3);

            // Kiểm tra và ép kiểu an toàn
            int id = (idObj instanceof Integer) ? (int) idObj : 0;
            String courseName = (nameObj instanceof String) ? (String) nameObj : "";

            Date beginDate = null, endDate = null;
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy"); // Định dạng phù hợp với dữ liệu

            try {
                if (beginDateObj instanceof Date) {
                    beginDate = (Date) beginDateObj;
                } else if (beginDateObj instanceof String) {
                    beginDate = sdf.parse((String) beginDateObj);
                }

                if (endDateObj instanceof Date) {
                    endDate = (Date) endDateObj;
                } else if (endDateObj instanceof String) {
                    endDate = sdf.parse((String) endDateObj);
                }
            } catch (ParseException e) {
                e.printStackTrace(); // Xử lý lỗi nếu định dạng sai
            }

            // Đưa dữ liệu lên giao diện
            tx_courseName.setText(courseName);
            if (beginDate != null) {
                cld_startDate.setDate(beginDate); // Đặt ngày bắt đầu vào JDateChooser
            }
            if (endDate != null) {
                cld_endDate.setDate(endDate); // Đặt ngày kết thúc vào JDateChooser
            }
        }
    }//GEN-LAST:event_table1MouseClicked

    private void bt_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_themActionPerformed
        // TODO add your handling code here:
        try {
            String courseName = tx_courseName.getText();
            Date beginDate = cld_startDate.getDate(); // Lấy ngày bắt đầu từ JDateChooser
            Date endDate = cld_endDate.getDate(); // Lấy ngày kết thúc từ JDateChooser

            if (courseName.isEmpty() || beginDate == null || endDate == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Chuyển đổi Date thành java.sql.Date
            java.sql.Date sqlBeginDate = new java.sql.Date(beginDate.getTime());
            java.sql.Date sqlEndDate = new java.sql.Date(endDate.getTime());

            Course newCourse = new Course(0, courseName, sqlBeginDate, sqlEndDate);
            boolean isAdded = courseService.addCourse(newCourse); // Gọi service để thêm khóa học

            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Thêm khóa học thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadCourses(); // Cập nhật lại danh sách khóa học
                reLoadText(); // Reset các input
            } else {
                JOptionPane.showMessageDialog(this, "Thêm khóa học thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm khóa học: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bt_themActionPerformed

    private void bt_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_suaActionPerformed
        try {
            int selectedRow = table1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn khóa học cần sửa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int id = (int) table1.getValueAt(selectedRow, 0); // Lấy ID của khóa học cần sửa
            String courseName = tx_courseName.getText();
            Date beginDate = cld_startDate.getDate();
            Date endDate = cld_endDate.getDate();

            if (courseName.isEmpty() || beginDate == null || endDate == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            java.sql.Date sqlBeginDate = new java.sql.Date(beginDate.getTime());
            java.sql.Date sqlEndDate = new java.sql.Date(endDate.getTime());

            Course updatedCourse = new Course(id, courseName, sqlBeginDate, sqlEndDate);
            boolean isUpdated = courseService.updateCourse(updatedCourse); // Kiểm tra kết quả

            if (isUpdated) {
                JOptionPane.showMessageDialog(this, "Cập nhật khóa học thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadCourses(); // Cập nhật lại danh sách
                reLoadText();
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật khóa học thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật khóa học: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bt_suaActionPerformed

    private void bt_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_xoaActionPerformed
        try {
            int selectedRow = table1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn khóa học cần xóa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int id = Integer.parseInt(table1.getValueAt(selectedRow, 0).toString());

            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa khóa học này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                boolean isDeleted = courseService.deleteCourse(id);
                if (isDeleted) {
                    JOptionPane.showMessageDialog(this, "Xóa khóa học thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    loadCourses(); // Cập nhật lại danh sách
                    reLoadText();
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa khóa học thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi xóa khóa học: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một khóa học để xóa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bt_xoaActionPerformed

